//  ----- Example 1 -----

let day = 0;

switch (day) {
    case 0:
        console.log("sunday");
        break;

    case 1:
        console.log("monday");
        break;

    case 2:
        console.log("tuesday");
        break;

    case 3:
        console.log("wednesday");
        break;

    case 4:
        console.log("thursday");
        break;

    case 5:
        console.log("friday");
        break;

    case 6:
        console.log("saturday");
        break;

    default:
        console.log("holiday");
}


//  ----- Example 2 -----

let grade = "B";

switch (grade) {
    case "A":
        console.log("Excellent");
        break;

    case "B":
        console.log("Very Good");
        break;

    case "C":
        console.log("Good");
        break;

    case "D":
        console.log("Needs Improvement");
        break;

    case "F":
        console.log("Fail");
        break;

    default:
        console.log("Invalid grade");
}
